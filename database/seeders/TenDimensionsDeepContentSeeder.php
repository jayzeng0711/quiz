    // =========================================================================
    // Helper
    // =========================================================================
    private function setDimensions(string $slug, array $dims): Quiz
    {
        $quiz = Quiz::where('slug', $slug)->firstOrFail();
        $meta = $quiz->meta ?? [];
        $meta['dimensions'] = $dims;
        $quiz->update(['meta' => $meta]);
        return $quiz;
    }

    private function setContent(string $slug, string $code, string $content): void
    {
        ResultType::whereHas('quiz', fn($q) => $q->where('slug', $slug))
            ->where('code', $code)
            ->update(['report_content' => $content]);
    }

